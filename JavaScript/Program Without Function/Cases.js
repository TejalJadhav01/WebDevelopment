// Program to write a LowerCase to UpperCase and UpperCase to LowerCase.


//LowerCase to UpperCase

var str=['a','b','c','d','e'];
for(let i=0;i<str.length;i++){
    console.log("Upper Case:" +str[i].toUpperCase());
}

//UpperCase to LowerCase

var str1=['A','B','C','D','E'];
for(let j=0; j<str1.length; j++){
    console.log("Lower Case:" +str1[j].toLowerCase());
}





